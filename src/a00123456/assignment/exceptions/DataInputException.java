package a00123456.assignment.exceptions;

public class DataInputException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DataInputException() {
		super();
		
	}

	public DataInputException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public DataInputException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DataInputException(String arg0) {
		super(arg0);
		
	}

	public DataInputException(Throwable arg0) {
		super(arg0);
		
	}

	

}
